#import "CSHProvinceNameCollectionViewCell.h"

@implementation CSHProvinceNameCollectionViewCell

- (void)awakeFromNib {
    self.contentView.backgroundColor = [UIColor clearColor];
    self.backgroundView = [[UIView alloc] initWithFrame:self.contentView.bounds];
    self.backgroundView.backgroundColor = [UIColor whiteColor];
    self.selectedBackgroundView = [[UIView alloc] initWithFrame:self.contentView.bounds];
    self.selectedBackgroundView.backgroundColor = [UIColor lightGrayColor];
    [self.selectedBackgroundView csh_setDefaultCornerRadius];
    [self.backgroundView csh_setDefaultCornerRadius];
    [self.contentView csh_setDefaultCornerRadius];
}

@end
