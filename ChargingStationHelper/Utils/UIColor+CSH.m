#import "UIColor+CSH.h"

@implementation UIColor (CSH)

+ (UIColor *)csh_brandColor {
    return [UIColor colorWithRed:0.0f green:180.0f/255.0f blue:174.0f/255.0f alpha:1.0];
}

+ (UIColor *)csh_activeColor {
    return [UIColor csh_activeColorWithAlpha:0.9];
}

+ (UIColor *)csh_activeColorWithAlpha:(CGFloat)alpha {
    return [UIColor blueColor];
}

@end
